/**
 * 검색조건 값 변경 되었을 경우의 처리
 */
function onChangingCondition(grdT2Master_refresh) {

  // 버튼 활성화 처리
  $NC.G_VAR.buttons._inquiry = "1";
  $NC.G_VAR.buttons._new = "0";
  $NC.G_VAR.buttons._save = "0";
  $NC.G_VAR.buttons._cancel = "0";
  $NC.G_VAR.buttons._delete = "0";
  $NC.G_VAR.buttons._print = "0";

  $NC.setInitTopButtons($NC.G_VAR.buttons);

  // 상품별 출고내역 화면
  G_GRDT1MASTER.lastRow = null;
  G_GRDT1MASTER.lastRowModified = false;
  $NC.setInitGridData(G_GRDT1MASTER);
  $NC.setGridDisplayRows("#grdT1Master", 0, 0);

  // 미출고사유등록 탭의 검색구분 변경 되어도 기간별 미출고 내역 탭의 그리드는 클리어 하지 않는다.
  if (grdT2Master_refresh || $NC.isNull(grdT2Master_refresh)) {
    // 배송처별 출고내역 화면
    G_GRDT1MASTER.lastRow = null;
    $NC.setInitGridData(G_GRDT2MASTER);
    $NC.setGridDisplayRows("#grdT2Master", 0, 0);
  }

}

