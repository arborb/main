/**
 * 미출고사유등록 그리드의 입력값 체크
 */
function grdT1MasterOnBeforePost(row) {

  if (!G_GRDT1MASTER.lastRowModified) {
    return true;
  }

  var rowData = G_GRDT1MASTER.data.getItem(row);
  if ($NC.isNull(rowData)) {
    return true;
  }

  if (rowData.CRUD != "R") {
    if ($NC.isNull(rowData.SHORTAGE_DIV)) {
      alert("미출고사유를 입력하십시오.");
      $NC.setGridSelectRow(G_GRDT1MASTER, {
        selectRow: row,
        activeCell: G_GRDT1MASTER.view.getColumnIndex("SHORTAGE_DIV_F"),
        editMode: true
      });
      return false;
    }
  }
  return true;
}