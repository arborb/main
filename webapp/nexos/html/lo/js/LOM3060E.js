function grdMasterInitialize() {

  var options = {
    frozenColumn: 0
  };

  // Grid Object, DataView 생성 및 초기화
  $NC.setInitGridObject("#grdMaster", {
    columns: grdMasterOnGetColumns(),
    queryId: "LOM3060E.RS_MASTER",
    sortCol: "ORDER_NO",
    gridOptions: options
  });
  G_GRDMASTER.view.onSelectedRowsChanged.subscribe(grdMasterOnAfterScroll);
}

function grdMasterOnAfterScroll(e, args) {

  var row = args.rows[0];
  if (G_GRDMASTER.lastRow != null) {
    if (row == G_GRDMASTER.lastRow) {
      e.stopImmediatePropagation();
      return;
    }
    if (!grdMasterOnBeforePost(G_GRDMASTER.lastRow)) {
      e.stopImmediatePropagation();
      return;
    }
  }

  // 에디터 값 세팅
  setInputValue("#grdMaster", G_GRDMASTER.data.getItem(row));

  // 상단 현재로우/총건수 업데이트
  $NC.setGridDisplayRows("#grdMaster", row + 1);
}

function grdMasterOnCellChange(e, args) {

  if (G_GRDMASTER.lastRow == null || G_GRDMASTER.data.getLength() === 0) {
    return;
  }
  var rowData = G_GRDMASTER.data.getItem(G_GRDMASTER.lastRow);
  if (rowData) {
    // 관리담당 처리
    switch (args.col) {
    case "ZIP_CD":
      var P_QUERY_PARAMS;
      var O_RESULT_DATA = [ ];
      if (!$NC.isNull(args.val)) {
        P_QUERY_PARAMS = {
          P_ADDR_NM: args.val
        };
        O_RESULT_DATA = $NP.getPostInfo({
          queryParams: P_QUERY_PARAMS,
          queryCanAll: true,
          errorMessage: "등록되어 있지 않은 우편번호입니다."
        });
      }
      if (O_RESULT_DATA.length <= 1) {
        onPostPopup(O_RESULT_DATA[0]);
      } else {
        $NP.showPostPopup({
          title: "우편번호 검색",
          columnTitle: ["우편번호", "주소"],
          queryParams: P_QUERY_PARAMS,
          queryCanAll: true,
          queryData: O_RESULT_DATA
        }, onPostPopup, onPostPopup);
      }
      return;
    case "ADDR_BASIC":
      rowData.SHIPPER_ADDR_BASIC = args.val;
      break;
    case "ADDR_DETAIL":
      rowData.SHIPPER_ADDR_DETAIL = args.val;
      break;
    case "SHIPPER_HP":
      rowData.SHIPPER_HP = args.val;
      break;
    case "SHIPPER_NM":
      rowData.SHIPPER_NM = args.val;
      break;
    }

    if (rowData.CRUD === "R") {
      rowData.CRUD = "U";
    }
    G_GRDMASTER.data.updateItem(rowData.id, rowData);

    // 마지막 선택 Row 수정 상태로 변경
    G_GRDMASTER.lastRowModified = true;
  }
}

